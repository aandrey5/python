print('------------------------------------------------------------------')
print('Программа будет суммировать ваше число n по схеме n + nn + nnn')
print('------------------------------------------------------------------')
n = input('Введите число n:  ')
print('------------------------------------------------------------------')
print('Ваше число будет суммироваться вот так: ', n, " + ", (n + n), " + ", (n + n + n))
print('------------------------------------------------------------------')
summ = int(n) + int(n + n) + int(n + n + n)
print('Итого получается: ', summ)
